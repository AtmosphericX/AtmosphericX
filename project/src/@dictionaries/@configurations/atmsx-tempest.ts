import * as loader from '../..';

export const getConfigurations = () => {
    const configurations = loader.modules.utilities.cfg();
    const settings = configurations?.sources?.miscellaneous_settings?.tempest_station;
    if (!settings?.enabled) { return null;}
    return { 
        api: settings?.api,
        deviceId: settings?.device,
        stationId: settings?.station,
        journal: false
    };
}